// ハローワールド — 最初のプログラム
//
// このアプリでは、左のレッスンを選ぶと中央のエディターにコードが表示されます。
// 右上の「▶ 実行」を押すと、このコードがその場で実行され、
// 右のペインに実行結果（標準出力）が表示されます。
//
// まずはそのまま実行してみましょう。次に "Hello, Gopherjuku!" の部分を
// 好きな文章に書き換えて、もう一度実行してみてください。
package main

import "fmt"

func main() {
	// fmt.Println は文字列を 1 行出力します（末尾に自動で改行が付きます）。
	fmt.Println("Hello, Gopherjuku!")

	// Printf は書式を指定して出力します。%d は整数、\n は改行を表します。
	for i := 1; i <= 3; i++ {
		fmt.Printf("%d 回目のこんにちは\n", i)
	}
}
